def suma(a, b):
    """Suma dos números"""
    return a + b

def resta(a, b):
    """Resta dos números"""
    return a - b

def multiplica(a, b):
    """Multiplica dos números"""
    return a * b

# Ejemplo de uso
print(suma(5, 3))        # Imprime 8
print(resta(10, 4))      # Imprime 6
print(multiplica(6, 7))  # Imprime 42